    template <class IT>
    std::size_t
    uleb128(IT& it, std::uint16_t val)
    {
        std::size_t count = 0;
        do
        {
            std::uint8_t byte = val & 0x7f;
            val >>= 7;
            if (val)
                byte |= 0x80;
            *it++ = byte;
            ++count;
        } while (val != 0);
        return count;
    }

