// Helper: Variable-length integer encoding (LEB128)
void
pushLeb128(std::vector<uint8_t>& buf, uint32_t val)
{
    do
    {
        uint8_t byte = val & 0x7F;
        val >>= 7;
        if (val != 0)
            byte |= 0x80;
        buf.push_back(byte);
    } while (val != 0);
}

// Helper: append bytes from a C-style array to a vector
// Uses a loop to avoid GCC false positive -Werror=stringop-overflow with insert()
template <std::size_t N>
void
appendBytes(std::vector<uint8_t>& buf, uint8_t const (&arr)[N])
{
    for (std::size_t i = 0; i < N; ++i)
        buf.push_back(arr[i]);
}

// Helper: append bytes from a vector to a vector
void
appendBytes(std::vector<uint8_t>& buf, std::vector<uint8_t> const& src)
{
    buf.insert(buf.end(), src.begin(), src.end());
}

// Helper: append a WASM section (ID + LEB128 size + content)
// extraSize is added to the encoded size (for trailing fill bytes)
void
appendSection(
    std::vector<uint8_t>& wasm,
    uint8_t sectionId,
    std::vector<uint8_t> const& content,
    uint32_t extraSize = 0)
{
    wasm.push_back(sectionId);
    pushLeb128(wasm, static_cast<uint32_t>(content.size() + extraSize));
    appendBytes(wasm, content);
}

